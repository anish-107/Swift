import Foundation

// Constant declaration with let
let myName : String

myName = "Anish"

print("My name is \(myName)")

// Type inference
let yourName = "Kevin"

print("Your name is \(yourName)")


// Variable declaration
var newName : String

newName = "Lautaro"

print(newName)

newName = "Immobile"

print(newName)

// Type inference
var marks = 89

print(marks)

marks = 99

print(marks)